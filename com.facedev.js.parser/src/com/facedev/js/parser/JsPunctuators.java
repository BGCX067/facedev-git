package com.facedev.js.parser;

/**
 * Enumerates all javascript punctuators.
 * 
 * @author alex.bereznevatiy@gmail.com
 *
 */
public interface JsPunctuators {
	
	String OPEN_BRACKET = "(";
	
	String CLOSE_BRACKET = ")";
	
	String OPEN_SQUARE_BRACKET = "[";
	
	String CLOSE_SQUARE_BRACKET = "]";
	
	String OPEN_CURVY_BRACKET = "{";
	
	String CLOSE_CURVY_BRACKET = "}";
	
	String COMA = ",";
	
	String COLON = ":";
	
	String DOT = ".";
	
	String PLUS = "+";
	
	String PLUSPLUS = "++";
	
	String MINUS = "-";
	
	String MINUSMINUS = "--";
	
	String TILDA = "~";
	
	String EXCLAMATION = "!";
	
	String MULTIPLICATION = "*";
	
	String DIVISION = "/";
	
	String MODULO = "%";
	
	String SHIFT_LEFT = "<<";
	
	String SHIFT_RIGHT = ">>";
	
	String SHIFT_RIGHT_UNSIGNED = ">>>";
	
	String LESS = "<";
	
	String GREATER = ">";
	
	String LESS_OR_EQUAL = "<=";
	
	String GREATER_OR_EQUAL = ">=";
	
	String EQUALS = "==";
	
	String NOT_EQUALS = "!=";
	
	String STRICT_EQUALS = "===";
	
	String STRICT_NOT_EQUALS = "!==";
	
	String AND = "&";
	
	String XOR = "^";
	
	String OR = "|";
	
	String ANDAND = "&&";
	
	String OROR = "||";
	
	String QUESTION = "?";
	
	String ASSIGNMENT = "=";
	
	String ASSIGNMENT_MUL = "*=";
	
	String ASSIGNMENT_DIV = "/=";
	
	String ASSIGNMENT_MOD = "%=";
	
	String ASSIGNMENT_SUM = "+=";
	
	String ASSIGNMENT_SUB = "-=";
	
	String ASSIGNMENT_SHL = "<<=";
	
	String ASSIGNMENT_SHR = ">>=";
	
	String ASSIGNMENT_USHR = ">>>=";
	
	String ASSIGNMENT_AND = ">>>=";
	
	String ASSIGNMENT_XOR = ">>>=";
	
	String ASSIGNMENT_OR = ">>>=";
}
