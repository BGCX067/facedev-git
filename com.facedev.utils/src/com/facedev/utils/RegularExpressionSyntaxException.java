package com.facedev.utils;

public class RegularExpressionSyntaxException extends Exception {
	private static final long serialVersionUID = -3972704203868049373L;

	public RegularExpressionSyntaxException() {
		super();
	}

	public RegularExpressionSyntaxException(String message, Throwable cause) {
		super(message, cause);
	}

	public RegularExpressionSyntaxException(String message) {
		super(message);
	}

	public RegularExpressionSyntaxException(Throwable cause) {
		super(cause);
	}
}
